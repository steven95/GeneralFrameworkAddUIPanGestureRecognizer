@implementation MessageController

- (void)viewDidLoad {
    [super viewDidLoad];
    MainTableView *mainTableView = [MainTableView new];
    self.tableView = mainTableView;
   [mainTableView registerClass:[MessageTableViewCell class] forCellReuseIdentifier:MessageCell];
   
}


#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {

    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {

    return 5;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    MessageTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"MessageCell" forIndexPath:indexPath];
    cell.backgroundColor = [UIColor redColor];
    // Configure the cell...
    
    return cell;
}


